<template>
  <div class="routing-rule-container">
    <hr>
    <KLabel :info="t('form.fields.headers.tooltip')">
      {{ t('form.fields.headers.label') }}
    </KLabel>
    <TransitionGroup name="appear">
      <div
        v-for="_, index in fieldsValue"
        :key="index"
        class="routing-rule-input"
      >
        <KInput
          v-model.trim="fieldsValue[index].header"
          :data-testid="`route-form-headers-name-input-${index + 1}`"
          :disabled="disabled"
          :placeholder="t('form.fields.headers.name.placeholder')"
        />
        <KInput
          v-model.trim="fieldsValue[index].values"
          :data-testid="`route-form-headers-values-input-${index + 1}`"
          :disabled="disabled"
          :placeholder="t('form.fields.headers.values.placeholder')"
        />
        <RoutingRulesEntitiesControls
          :disabled="disabled"
          :is-add-disabled="index !== fieldsValue.length - 1"
          :routing-rules-entity="RoutingRulesEntities.HEADERS"
          @add="$emit('add')"
          @remove="$emit('remove', index)"
        />
      </div>
    </TransitionGroup>
  </div>
</template>

<script setup lang="ts">
import type { PropType } from 'vue'
import { ref, watch } from 'vue'
import type { HeaderFields } from '../types'
import { RoutingRulesEntities } from '../types'
import composables from '../composables'
import RoutingRulesEntitiesControls from './RoutingRulesEntitiesControls.vue'

/** Local types, not exported
 * Same pattern used other fields components
 */
type FieldsValue = HeaderFields[]

const props = defineProps({
  modelValue: {
    type: Array as PropType<FieldsValue>,
    required: true,
  },
  disabled: {
    type: Boolean,
    default: false,
  },
})

const emit = defineEmits<{
  (e: 'add'): void,
  (e: 'remove', index: number): void,
  (e: 'update:modelValue', value: FieldsValue): void,
}>()

const { i18n: { t } } = composables.useI18n()

const fieldsValue = ref<FieldsValue>([])

watch(props.modelValue, (value) => {
  fieldsValue.value = [...value]
}, { immediate: true, deep: true })

watch(fieldsValue, (value) => {
  emit('update:modelValue', value)
}, { deep: true })
</script>

